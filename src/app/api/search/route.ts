export async function POST(request: NextRequest) {
  try {
    const { query, category } = await request.json();
    
    if (!query) {
      return NextResponse.json(
        { error: '검색어가 필요합니다.' },
        { status: 400 }
      );
    }

    const searchQuery = query.toLowerCase();
    const results = [];

    for (const service of sample_ai_services) {
      // 카테고리 필터링
      if (category && service.category !== category) {
        continue;
      }

      // 검색어 매칭
      if (
        searchQuery.includes(service.name.toLowerCase()) ||
        searchQuery.includes(service.description.toLowerCase()) ||
        service.features.some(feature => 
          searchQuery.includes(feature.toLowerCase())
        ) ||
        searchQuery.includes(service.category.toLowerCase()) ||
        searchQuery.includes(service.source.toLowerCase())
      ) {
        results.push(service);
      }
    }

    return NextResponse.json({
      results,
      total: results.length
    });

  } catch (error) {
    console.error('Search API Error:', error);
    return NextResponse.json(
      { error: '서버 오류가 발생했습니다.' },
      { status: 500 }
    );
  }
} 